"""Классификация колонок по роли: что количественное, что категориальное.

Определяет, какой метод описания и какой тест применять по протоколу. Имена
сверяются с реальными колонками очищенного датасета в classify(), при
расхождении выдается предупреждение.
"""

from . import config

# Служебные колонки и идентификаторы, в анализ признаков не идут.
IDENTIFIERS = [
    "N пп",
    "ИБ",
    "ИБ_исходный",
    "Дата рождения",
]

# Количественные показатели (для них нормальность, M/SD/CI или Me/Q1-Q3).
QUANTITATIVE = [
    "Возраст (на текущий момент)",
    "Длительность СД (лет)",
    "Возраст манифестации СД",
    "Суточная доза инсулина",
    "HbA1c",
    "Креатинин при поступлении",
    "Мочевина при поступлении",
    "pH при поступлении",
    "ВЕ при поступлении",
    "Лактат при поступлении",
    "Калий при поступлении",
    "Натрий при поступлении",
    "Глюкоза при поступлении",
    "Общий холестерин",
    "ЛПНП",
    "ЛПВП",
    "ТГ",
]

# Категориальные показатели (абсолютные значения, доли, 95% ДИ Клоппера-Пирсона).
# Сюда входят бинарные и порядковые, кодированные числами.
CATEGORICAL = [
    "тип СД (1-1, 2-2, 3 -др)",
    "Пол (0 - Ж, 1 - М)",
    "Вид инсулинотерапии (1 - ручки, 2 - помпа) на момент ДКА",
    "ХБП, С",
    "ХБП, А",
    "Невролог",
    "Ретинопатия (0 - нет, 1 - непролиферативная, 2 - препролиферативная, 3 - пролиферативная)",
    "Степень тяжести ДКА",
    "Алкоголь за сутки до ДКА (0 - нет, 1 - да)",
]

TARGET = config.TARGET_COLUMN


def classify(df) -> dict:
    """Возвращает словарь ролей и предупреждает о колонках вне классификации."""
    known = set(IDENTIFIERS + QUANTITATIVE + CATEGORICAL + [TARGET])
    actual = set(df.columns)

    missing = known - actual
    unclassified = actual - known
    if missing:
        print(f"Внимание: в данных нет колонок из классификации: {sorted(missing)}")
    if unclassified:
        print(f"Внимание: колонки без классификации: {sorted(unclassified)}")

    return {
        "identifiers": [c for c in IDENTIFIERS if c in actual],
        "quantitative": [c for c in QUANTITATIVE if c in actual],
        "categorical": [c for c in CATEGORICAL if c in actual],
        "target": TARGET if TARGET in actual else None,
    }
